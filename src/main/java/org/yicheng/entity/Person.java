package org.yicheng.entity;

public class Person {
    protected Integer id;
    protected String name;
    public Person(){

    }
    public Person(String name){
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
